def count_occurrences(phrase: str, letter: str) -> int:
    """
    Підрахунок входжень символу або підрядка
    у фразі без урахування регістру.

    Args:
        phrase (str): Вхідний рядок, у якому виконується пошук.
        letter (str): Символ або підрядок для пошуку.
            - Якщо передано порожній рядок (""),
            результат відповідає поведінці str.count("").
            - Якщо передано кілька символів, рахується підрядок.

    Returns:
        int: Кількість входжень (без урахування регістру).

    Notes:
        Дзеркальне відображення поведінки вбудованого str.count
        з приведенням до нижнього регістру.
    """
    return phrase.lower().count(letter.lower())
